# In Python, string is an immutable sequence data type. It is the sequence of Unicode characters wrapped inside 
# single, double, or triple quotes

str1='Welcome to Python' 
print(str1)

str2="Welcome to Python" 
print(str2)

str1='''This is 
the first
Multi-line string.
'''
print(str1)

# Quotes in String
str1='Welcome to "Python Tutorial" at Nextgen'
print(str1)

str2="Welcome to 'Python Tutorial' at Nextgen"
print(str2)

#  Escape Sequence

str1='Welcome to \'Python Tutorial\' ' # single quote
print(str1)

str2="Welcome to \"Python Tutorial\"" # double quote
print(str2)